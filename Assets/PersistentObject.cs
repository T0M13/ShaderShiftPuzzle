using UnityEngine;

public class PersistentObject : MonoBehaviour
{
    private static PersistentObject instance = null;

    private void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(this.gameObject);
        }
        else if (instance != this)
        {
            Destroy(gameObject);
        }
    }
}
